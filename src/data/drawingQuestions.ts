export function getDailyDrawingIndex(date: Date): number {
  const start = new Date(date.getFullYear(), 0, 0);
  const diff = date.getTime() - start.getTime();
  const dayOfYear = Math.floor(diff / (1000 * 60 * 60 * 24));
  return dayOfYear % drawingQuestions.length;
}

export function getTodayDrawingQuestion(): string {
  // KST 기준 (기존 HomeContext와 동일한 KST 계산 패턴)
  const now = new Date();
  const kstOffset = 9 * 60 * 60 * 1000;
  const kstNow = new Date(
    now.getTime() + now.getTimezoneOffset() * 60000 + kstOffset,
  );
  return drawingQuestions[getDailyDrawingIndex(kstNow)];
}
